package com.bstek.ureport.export.html;

public class InputComponentData {

	private String uuid;
	
	private String label;
	
	private String bindParameter;
	
	private String type;
	
	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getBindParameter() {
		return bindParameter;
	}

	public void setBindParameter(String bindParameter) {
		this.bindParameter = bindParameter;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
}
